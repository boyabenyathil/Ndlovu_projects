numbers = [3, 3, 5, 6, 7, 8, 8, 10]


#remove element
numbers.remove(10)
print(numbers)

#add element
numbers.append(11)
print(numbers)

#inserting element to specified index
numbers.insert(2, 19)
print(numbers)

#sorting list in assending order
numbers.sort()
print(numbers)

#sorting list in desending order
numbers.reverse()
print(numbers)